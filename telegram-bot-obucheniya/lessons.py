def markdown_to_html(text: str) -> str:
    """Конвертация Markdown в HTML для рендеринга в Telegram."""
    lines = text.split("\n")
    processed_lines = []
    in_quote = False
    quote_block = []
    
    for line in lines:
        stripped = line.strip()
        if stripped.startswith(">"):
            if not in_quote:
                in_quote = True
            content = stripped[1:].strip()
            quote_block.append(content)
        else:
            if in_quote:
                in_quote = False
                processed_lines.append(f"<blockquote>" + "\n".join(quote_block) + "</blockquote>")
                quote_block = []
            processed_lines.append(line)
            
    if in_quote:
        processed_lines.append(f"<blockquote>" + "\n".join(quote_block) + "</blockquote>")
        
    html = "\n".join(processed_lines)
    
    # Замена **жирного** на <b>жирный</b>
    html = re.sub(r"\*\*(.*?)\*\*", r"<b>\1</b>", html)
    # Замена *курсива* на <i>курсив</i>
    html = re.sub(r"\*(.*?)\*", r"<i>\1</i>", html)
    return html

