def get_lesson_content(block_id: int, lesson_id: int) -> str:
    """Получение содержимого урока с авто-конвертацией в HTML."""
    if block_id in BLOCKS and lesson_id in BLOCKS[block_id]["lessons"]:
        raw_content = BLOCKS[block_id]["lessons"][lesson_id]["content"]
        # Добавляем вверху красивый заголовок
        title = BLOCKS[block_id]["lessons"][lesson_id]["title"]
        header = f"📚 <b>Блок {block_id}. Урок {lesson_id}: {title}</b>\n\n"
        return header + markdown_to_html(raw_content)
    return "Урок не найден"

def get_homework_description(block_id: int) -> str:
    """Получение домашнего задания для блока."""
    if block_id in BLOCKS:
        for lesson_data in BLOCKS[block_id]["lessons"].values():
            if "homework" in lesson_data:
                raw_content = lesson_data["homework"]["content"]
                header = f"📝 <b>Домашнее задание Блока {block_id}</b>\n\n"
                return header + markdown_to_html(raw_content)
    return "Домашнее задание для этого блока не найдено."

def get_homework_script_help(block_id: int) -> Optional[str]:
    """Получение помощи по скрипту для домашнего задания (если есть)."""
    if block_id in BLOCKS:
        for lesson_data in BLOCKS[block_id]["lessons"].values():
            if "homework" in lesson_data and "script_help" in lesson_data["homework"]:
                return markdown_to_html(lesson_data["homework"]["script_help"])
    return None

def get_homework_type(block_id: int) -> str:
    """Определение типа домашнего задания: voice или text."""
    if block_id in BLOCKS:
        for lesson_data in BLOCKS[block_id]["lessons"].values():
            if "homework" in lesson_data:
                return lesson_data["homework"].get("type", "text")
    return "text"

