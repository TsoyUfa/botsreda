@app.route("/api/start", methods=["POST"])
def start_session():
    data = request.json or {}
    profile_id = data.get("profile_id")
    if profile_id not in CLIENT_PROFILES:
        return jsonify({"error": "Invalid profile ID"}), 400
    
    session_id = os.urandom(8).hex()
    profile = CLIENT_PROFILES[profile_id]
    
    sessions[session_id] = {
        "profile_id": profile_id,
        "history": [
            {"role": "system", "content": profile["prompt"]}
        ],
        "chat_history": [] # Contains client/broker messages
    }
    
    greeting = profile["greeting"]
    sessions[session_id]["history"].append({"role": "model", "content": greeting})
    sessions[session_id]["chat_history"].append({
        "sender": "client", 
        "text": greeting,
        "emotion": "neutral",
        "sentiment_score": 3
    })
    
    return jsonify({
        "session_id": session_id, 
        "greeting": greeting,
        "gender": profile.get("gender", "male")
    })

@app.route("/api/chat", methods=["POST"])
def chat():
    data = request.json or {}
    session_id = data.get("session_id")
    message = data.get("message", "").strip()
    
    if session_id not in sessions:
        return jsonify({"error": "Session not found"}), 404
    if not message:
        return jsonify({"error": "Empty message"}), 400
        
    session = sessions[session_id]
    
    # Save broker message
    session["history"].append({"role": "user", "content": message})
    session["chat_history"].append({"sender": "broker", "text": message})
    
    # If API key is not configured, return mock reply
    if not is_api_key_valid:
        reply = {
            "reply": f"[Имитация ответа клиента]: Я услышал вас, но все же сомневаюсь. Дайте мне более конкретные расчеты.",
            "emotion": "skeptical",
            "sentiment_score": 2
        }
        session["history"].append({"role": "model", "content": reply["reply"]})
        session["chat_history"].append({
            "sender": "client", 
            "text": reply["reply"],
            "emotion": reply["emotion"],
            "sentiment_score": reply["sentiment_score"]
        })
        return jsonify(reply)
        
    try:
        # Prepare structured dialog history for Gemini
        contents = []
        for h in session["history"]:
            if h["role"] == "system":
                continue
            role = "user" if h["role"] == "user" else "model"
            contents.append({"role": role, "parts": [h["content"]]})
            
        system_instruction = (
            f"{CLIENT_PROFILES[session['profile_id']]['prompt']}\n\n"
            "ВАЖНО: Ты должен возвращать ответ СТРОГО в формате JSON с полями:\n"
            "- 'reply' (строка, твой ответ брокеру на русском языке)\n"
            "- 'emotion' (строка, твое текущее эмоциональное состояние: 'skeptical', 'angry', 'neutral', 'interested', 'satisfied')\n"
            "- 'sentiment_score' (число от 1 до 5, отражающее уровень твоего доверия к брокеру, где 1 - минимальный, 5 - максимальный)\n"
            "Никакого другого текста, кроме валидного JSON, возвращать нельзя."
        )
        
        model = genai.GenerativeModel(
            model_name='gemini-1.5-flash',
            system_instruction=system_instruction
        )
        
        response = model.generate_content(
            contents,
            generation_config={
                "response_mime_type": "application/json",
                "response_schema": {
                    "type": "OBJECT",
                    "properties": {
                        "reply": {"type": "STRING", "description": "Client response to the broker"},
                        "emotion": {
                            "type": "STRING", 
                            "enum": ["skeptical", "angry", "neutral", "interested", "satisfied"]
                        },
                        "sentiment_score": {"type": "INTEGER", "description": "Score from 1 to 5"}
                    },
                    "required": ["reply", "emotion", "sentiment_score"]
                }
            }
        )
        
        result = json.loads(response.text.strip())
        
        # Save model reply in history
        session["history"].append({"role": "model", "content": result["reply"]})
        session["chat_history"].append({
            "sender": "client", 
            "text": result["reply"],
            "emotion": result["emotion"],
            "sentiment_score": result["sentiment_score"]
        })
        
        return jsonify(result)
    except Exception as e:
        print(f"Gemini API error: {e}")
        return jsonify({"error": f"Gemini API error: {str(e)}"}), 500

